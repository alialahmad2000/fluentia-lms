-- SPELLING LAB seed (prompt 09, Surface 3) — idempotent, re-runnable.
-- Source A: curriculum_vocabulary (primary — what students are already learning).
--   Column map: word→word_en, pronunciation_ipa→ipa, definition_ar→meaning_ar,
--   example_sentence→example_en, id→source_vocab_id. Single clean words only.
-- Source B/C appended below (common + phonetic_traps), authored in-session.
-- ════════════════════════════════════════════════════════════════════════════
INSERT INTO public.spelling_lab_words (word_en, difficulty, category, source_vocab_id, audio_url, ipa, meaning_ar, example_en)
SELECT DISTINCT ON (lower(cv.word))
  cv.word,
  CASE WHEN char_length(cv.word) <= 4 THEN 2
       WHEN char_length(cv.word) <= 6 THEN 4
       WHEN char_length(cv.word) <= 9 THEN 6
       ELSE 8 END,
  'curriculum',
  cv.id,
  cv.audio_url,
  cv.pronunciation_ipa,
  cv.definition_ar,
  cv.example_sentence
FROM public.curriculum_vocabulary cv
WHERE cv.word ~ '^[A-Za-z]{2,15}$'
ORDER BY lower(cv.word), (cv.audio_url IS NOT NULL) DESC, cv.id
ON CONFLICT (word_en) DO NOTHING;

