async function run() {
  const { data: l1 } = await sb.from('curriculum_levels').select('id').eq('level_number', 1).single()
  const { data: units } = await sb.from('curriculum_units')
    .select('id, unit_number, brief_generated_at').eq('level_id', l1.id).order('unit_number')

  let briefDone = 0, ribbonDone = 0

  // Phase B: Briefs
  console.log('\n=== PHASE B: L1 BRIEFS ===')
  for (const content of L1_CONTENT) {
    const unit = (units || []).find(u => u.unit_number === content.unit_number)
    if (!unit) { console.log('SKIP: unit ' + content.unit_number + ' not found'); continue }
    if (unit.brief_generated_at) { console.log('SKIP: unit ' + content.unit_number + ' already has brief'); briefDone++; continue }

    const { data: updated, error } = await sb
      .from('curriculum_units')
      .update({
        why_matters: content.why_matters,
        outcomes: content.outcomes,
        brief_questions: content.brief_questions,
        brief_generated_at: new Date().toISOString(),
        brief_locale: 'ar',
      })
      .eq('id', unit.id)
      .select('id')

    if (error) { console.error('ERROR unit ' + content.unit_number, error.message); continue }
    if (!updated || updated.length !== 1) { console.error('ROWCOUNT FAIL unit ' + content.unit_number); continue }

    briefDone++
    console.log('✓ L1 Unit ' + content.unit_number + ' brief done. chars:' + content.why_matters.length + ' outcomes:' + content.outcomes.length)
  }
  console.log('=== L1 BRIEFS COMPLETE: ' + briefDone + '/12 ===')

  // Phase C: Ribbons
  console.log('\n=== PHASE C: L1 RIBBONS ===')
  for (const content of L1_CONTENT) {
    const unit = (units || []).find(u => u.unit_number === content.unit_number)
    if (!unit) continue

    const { data: updated, error } = await sb
      .from('curriculum_units')
      .update({
        activity_ribbons: content.ribbons,
        ribbons_generated_at: new Date().toISOString(),
      })
      .eq('id', unit.id)
      .select('id')

    if (error) { console.error('ERROR ribbons unit ' + content.unit_number, error.message); continue }
    if (!updated || updated.length !== 1) { console.error('ROWCOUNT FAIL ribbons unit ' + content.unit_number); continue }

    ribbonDone++
    console.log('✓ L1 Unit ' + content.unit_number + ' ribbons done (' + Object.keys(content.ribbons).length + ' activities)')
  }
  console.log('=== L1 RIBBONS COMPLETE: ' + ribbonDone + '/12 ===')
}

run().catch(console.error)
